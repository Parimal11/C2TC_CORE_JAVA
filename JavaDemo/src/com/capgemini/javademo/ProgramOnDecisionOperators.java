package com.capgemini.javademo;

//class name ProgramOnDecisionOperators is declared
public class ProgramOnDecisionOperators {

	//main method
	public static void main(String[] args) {
		
		//integer variable first is declared with value 5
		int first = 5;
        
		//Control Statement 'if' which checks condition first != 5
		
		if(first != 5){
			
			/*Executed if value of first is not equal to 5
      	  	  Prints The result
      	  	*/
			System.out.println("Value of 'first' is not 5");
      	  }
        
		//Control Statement 'else' is called first == 5
        else{
        	
        	/*Executed if value of first is  equal to 5
    	  	  Prints The result
    	  	*/
        	System.out.println("Value of x is 5");
        }


	}

}
