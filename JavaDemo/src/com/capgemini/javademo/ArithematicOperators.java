//This Program shows Arithematic Operators used in Java

package com.capgemini.javademo;


//class name ArithematicOperators is declared
public class ArithematicOperators {

	//main method
	public static void main(String[] args) {
		
		
		//declares integer variable firstNo
		int firstNo = 10; 
		
		//declares integer variable secondNo
		int secondNo = 20;
		
		// Performs Addition Operation with '+' operators and prints result
		System.out.println(firstNo + secondNo);
		
		// Performs Pre-decrement Operation with '--' operators and prints result
		System.out.println(--firstNo);
		
		// Performs Pre-increment Operation with '++' operators and prints result
		System.out.println(++secondNo);

	}

}
