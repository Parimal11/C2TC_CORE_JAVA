//This Program shows Typecasting used in Java
package com.capgemini.javademo;


//class name ProgramOnTypecasting is declared
public class ProgramOnTypecasting {

	//main method
	public static void main(String[] args) {
		
		//float variable firstNumber is declared with value 10.525f
		float firstNumber = 10.532f;
		
		//secondNumber variable will be Typecasting to long from float
	    long secondNumber = (long) firstNumber;
	    
	    //Prints Second Number
	    System.out.println(secondNumber);
	    


	}

}
